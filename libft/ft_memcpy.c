#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*pdst;
	unsigned char	*psrc;

	pdst = (unsigned char *)dst;
	psrc = (unsigned char *)src;
	if (n == 0 || src == dst)
	{
		return (dst);
	}
	while (n-- > 0)
	{
		*(pdst++) = *(psrc++);
	}
	return (dst);
}
