#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		cont;
	t_list	*ptr;

	cont = 0;
	ptr = lst;
	while (ptr)
	{
		ptr = ptr->next;
		cont++;
	}
	return (cont);
}
